#!/usr/bin/env python3
import rclpy
from rclpy.node import Node

from geometry_msgs.msg import PoseStamped, PoseArray
from nav_msgs.msg import Path, Odometry


class TrajectoryVisualizer(Node):
    """
    This node visualizes two things in RViz:
    1. The planned/smoothed trajectory generated by the trajectory generator node.
    2. The actual path taken by the robot based on Odometry feedback.

    It publishes:
        /visualized_trajectory  → Path showing the planned trajectory
        /robot_path             → Path showing the executed trajectory
    """

    def __init__(self):
        super().__init__("trajectory_visualizer")

        # -----------------------------
        # Subscribers
        # -----------------------------
        # Subscribe to the generated /trajectory (PoseArray)
        self.create_subscription(
            PoseArray,
            "/trajectory",
            self.trajectory_callback,
            10
        )

        # Subscribe to Odometry to record the robot's path
        self.create_subscription(
            Odometry,
            "/odom",
            self.odom_callback,
            10
        )

        # -----------------------------
        # Publishers
        # -----------------------------
        # For visualizing the planned trajectory in RViz
        self.traj_pub = self.create_publisher(Path, "/visualized_trajectory", 10)

        # For visualizing the actual robot path (trail)
        self.robot_path_pub = self.create_publisher(Path, "/robot_path", 10)

        # -----------------------------
        # Internal storage
        # -----------------------------
        # Store the robot's executed path
        self.robot_path = Path()
        self.robot_path.header.frame_id = "map"

        self.get_logger().info("Trajectory Visualizer Node Started.")

    # ==========================================================
    # 1. Convert planned trajectory (PoseArray) → nav_msgs/Path
    # ==========================================================
    def trajectory_callback(self, msg: PoseArray):
        """
        Convert the planned PoseArray trajectory into a Path message
        so it can be displayed cleanly in RViz.
        """
        path_msg = Path()
        path_msg.header.frame_id = "map"

        for pose in msg.poses:
            ps = PoseStamped()
            ps.header.frame_id = "map"
            ps.pose.position.x = pose.position.x
            ps.pose.position.y = pose.position.y
            path_msg.poses.append(ps)

        self.traj_pub.publish(path_msg)

    # ==========================================================
    # 2. Record and publish robot's actual executed path
    # ==========================================================
    def odom_callback(self, msg: Odometry):
        """
        Append each new robot pose (from Odometry) to a Path message.
        This builds a trail of the real path followed by the robot.
        """
        ps = PoseStamped()
        ps.header = msg.header
        ps.header.frame_id = "map"  # enforce visualization in map frame
        ps.pose = msg.pose.pose     # copy pose from odom

        self.robot_path.poses.append(ps)

        # Publish updated executed path
        self.robot_path_pub.publish(self.robot_path)


def main(args=None):
    rclpy.init(args=args)
    node = TrajectoryVisualizer()
    rclpy.spin(node)
    node.destroy_node()
    rclpy.shutdown()


if __name__ == "__main__":
    main()
